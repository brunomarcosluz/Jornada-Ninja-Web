// Quarto exercício da lista, objetivo: criar uma função que retorne o módulo (resto) da divisão

/* 4-) Crie uma função que irá receber dois valores, o dividendo e o divisor. A função deverá imprimir o resultado
e o resto da divisão destes dois valores.
*/

function restoDe(dividendo,divisor) {
    console.log(`o resto de ${dividendo} e ${divisor} é:`, dividendo % divisor);
}

restoDe(8,3);
restoDe(10,2);
