// Operadores: Atribuição

/* A atribuição em Javascript se assemelha com as demais 
linguagens, sem muito mistério: */

var b = 4 // (=) sinal de atribuição

// podemos fazer operações com o sinal de atribuição:

let a = 2

b += a // seria o mesmo de b = b + a
console.log(b);
b -= a // b = b - a
console.log(b);
b *= a
console.log(b);
b /= a
console.log(b);
b %= 2
console.log(b);