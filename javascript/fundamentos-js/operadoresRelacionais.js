// Estudo sobre Operadores Relacionais e como se comportam em JS


/* Operadores relacionais sempre irão retornar o valor
true ou false ou seja, sempre vão retornar se é 
verdadeiro ou falso seguimos aos exemplo: */

var a = 1
var b = '1'

console.log(a == b); // variável a é igual a b? true
console.log(a === b); // variável a é estritamente igual a b? false

/* São os operadores relacionais: */

console.log(a<b); // menor que
console.log(a>b); // maior que
console.log(a<=b); // menor ou igual
console.log(a>=b); // maior ou igual

