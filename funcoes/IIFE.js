/* IIFE = Immediately Invoked Function Expression 

Ou funções imediatamente invocadas.

Resumidamente são funções dentro de () que são 
executadas logo após a sua declaração, além de fugir do escopo
global que é uma boa! */

(function() {
    console.log('É executado na hora!')
    console.log('Foge do escopo global!')
})()

